/**
 * 카테고리 + 성별 + 사이즈로 표준 치수를 반환.
 * 없는 조합은 남성 기준으로 fallback.
 */
export function getSizeChart(
  category: string,
  gender: GenderType,
  size: SizeLabel
): Record<string, number> {
  const isFemale = gender === "female";

  switch (category) {
    case "top":
    case "outer":
      return isFemale ? FEMALE_TOP[size] : MALE_TOP[size];
    case "bottom":
      return isFemale ? FEMALE_BOTTOM[size] : MALE_BOTTOM[size];
    case "dress":
      return FEMALE_DRESS[size];
    default:
      return isFemale ? FEMALE_TOP[size] : MALE_TOP[size];
  }
}

